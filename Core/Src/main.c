void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc)
{
	static uint16_t adc_counter=0;
   if(hadc==&hadc1)
	 {
		 Adc_Sample_Calculations((uint32_t *)&ADC_DATA[0]);
		 adc_counter++;
		 
		 if(adc_counter==TOTAL_NO_SAMPLE)
		 {
			 //store adc accumulated data after every 100 ms.
			 Store_Adc_Data();
			 adc_counter=0;
			 acc_data_ready=true;
			 
		 }
	 }
}

